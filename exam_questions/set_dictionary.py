my_dict = {"name": "Alice", "age": 30, "city": "Paris"}
print(my_dict["name"])  # Output: Alice
my_dict["occupation"] = "Software Engineer"  # Add a key-value pair
print(my_dict)  # Output: {'name': 'Alice', 'age': 30, 'city': 'Paris', 'occupation': 'Software Engineer'}
del my_dict["city"]  # Remove a key-value pair
print(my_dict)  # Output: {'name': 'Alice', 'age': 30, 'occupation': 'Software Engineer'}
